#include<stdio.h>
#include<math.h>
int main()
{
    int n,i,arr[100],x=0,y=0,m;
    scanf("%d",&n);
    for(i=0;i<n;i++)
    {
        scanf("%d",&arr[i]);
    }
    for(i=0;i<n;i++)
    {
        if(i%2==0)
        {
            x=x+arr[i];
        }
        else
        {
            y=y+arr[i];
        }
    }
   m=abs(x-y);
   if(m%4==0)
   {
       printf("X");
   }
   else
   {
       printf("Y");
   }
}